require 'pry'                  # => true
a = [[1,2],[1,4],[2,3],[2,4]]  # => [[1, 2], [1, 4], [2, 3], [2, 4]]

b = a.reduce(Hash.new([])) do |total, group|  # => [[1, 2], [1, 4], [2, 3], [2, 4]]
  total[group[0]] += [group[1]]               # => [2], [2, 4], [3], [3, 4]
  total                                       # => {1=>[2]}, {1=>[2, 4]}, {1=>[2, 4], 2=>[3]}, {1=>[2, 4], 2=>[3, 4]}
end                                           # => {1=>[2, 4], 2=>[3, 4]}

b  # => {1=>[2, 4], 2=>[3, 4]}
